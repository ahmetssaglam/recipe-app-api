def add(x, y):
    """
    sum two number
    :param x: first number
    :param y: second number
    :return: sum of numbers
    """
    return x + y


def sub(x, y):
    """
    remove y from x
    :param x:
    :param y:
    :return: x - y
    """
    return x - y
